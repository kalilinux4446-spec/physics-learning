// =====================================================================

export function getUnitById(id: string): Unit | undefined {
  return PHYSICS_BOOK.find(u => u.id === id);
}

export function getLessonById(id: string): Lesson | undefined {
  for (const unit of PHYSICS_BOOK) {
    const lesson = unit.lessons.find(l => l.id === id);
    if (lesson) return lesson;
  }
  return undefined;
}

export function getUnitByLessonId(lessonId: string): Unit | undefined {
  return PHYSICS_BOOK.find(u => u.lessons.some(l => l.id === lessonId));
}

export function getAllLessons(): Lesson[] {
  return PHYSICS_BOOK.flatMap(u => u.lessons);
}
